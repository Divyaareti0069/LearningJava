package javaDataTypes;

public class WhileAndDoWhileLoops {

	public static void main(String[] args) {
		
		        int i = 1;
		        System.out.println("While Loop:");
		        while (i <= 15) {
		            System.out.println(i);
		            i++;
		        }

		        int j = 1;
		        System.out.println("\nDo-While Loop:");
		        do {
		            System.out.println(j);
		            j++;
		        } while (j <= 5);
		    }
		}


